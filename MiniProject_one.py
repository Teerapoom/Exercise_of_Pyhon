print("คำนวณราคาขาย โดยกรอกราคาต้นทุน เเละกรอก % กำไรมราต้องการ โปรเเกรมจะคำนวณออกมาว่าต้องขายในราคาเท่าไร")
cost = float(input("ต้นทุน: "))
profile = int(input("ต้องการกำไร   "))
cal = (cost/(100 - profile))*100
print("ต้นทุน %.2f  บาท" %cost)
print("ต้องการกำไร", profile ,"%","ของราคาขาย")
print("ต้องขายในราคา %.2f  บาท " %cal)
